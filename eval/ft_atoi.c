#include <stdio.h>
#include <stdlib.h>

int	ft_atoi(char *str)
{
	long	n;
	int	signal;
	int	i;

	signal = 1;
	n = 0;
	i = 0;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signal *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + (str[i] - '0');
		i++;
	}
	return (n * signal);
}

int	main(int ac, char **av)
{
	printf("ft_atoi: %d\n", ft_atoi(av[1]));
	printf("atoi: %d", atoi(av[1]));
	return (0);
}
